package com.gleason.apahelper;

public class Line {
	private float startingX;
	private float startingY;
	private float endingX;
	private float endingY;
	/**
	 * @return the startingX
	 */
	public float getStartingX() {
		return startingX;
	}
	/**
	 * @param startingX the startingX to set
	 */
	public void setStartingX(float startingX) {
		this.startingX = startingX;
	}
	/**
	 * @return the startingY
	 */
	public float getStartingY() {
		return startingY;
	}
	/**
	 * @param startingY the startingY to set
	 */
	public void setStartingY(float startingY) {
		this.startingY = startingY;
	}
	/**
	 * @return the endingX
	 */
	public float getEndingX() {
		return endingX;
	}
	/**
	 * @param endingX the endingX to set
	 */
	public void setEndingX(float endingX) {
		this.endingX = endingX;
	}
	/**
	 * @return the endingY
	 */
	public float getEndingY() {
		return endingY;
	}
	/**
	 * @param endingY the endingY to set
	 */
	public void setEndingY(float endingY) {
		this.endingY = endingY;
	}
}
